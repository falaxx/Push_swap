int		ft_is_prime(int nb)
{
	int i;

	i = 3;
	if (nb <= 1)
		return (0);
	if (nb == 2)
		return (1);
	if ((nb % 2) == 0)
		return (0);
	while (i < (nb / 2))
	{
		if ((nb % i) != 0)
			i = i + 2;
		else
			return (0);
	}
	return (1);
}
